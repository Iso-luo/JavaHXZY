class Demo{
	public static void main(String[] args){
	
		//   定义一个int 类型的变量 名字是age 值是21
		int age =21;
		//  定义一个long 类型的变量 名字是 ID 值是11111111
		long id =1111L;
		
		System.out.println(age);
		System.out.println(id);
		//定义一个float 类型变量 
		float f = 3.34;
		//定义一个double 类型的变量 
		double d = 3.14;
		System.out.println(f);
		System.out.println(d);
		
	}
	
}